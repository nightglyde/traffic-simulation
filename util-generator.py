import math

MULTIPLIER = 100000
MULTIPLIER_SQUARED = MULTIPLIER**2

side_lengths = {}

print("side_lengths = {")

for angle in range(90):
    x = math.cos(math.radians(angle))*MULTIPLIER
    y = math.sqrt(MULTIPLIER_SQUARED - (x**2))

    x = round(x)
    y = round(y)

    side_lengths[angle]     = ( x,  y)
    side_lengths[angle+90]  = (-y,  x)
    side_lengths[angle+180] = (-x, -y)
    side_lengths[angle+270] = ( y, -x)

    print("{:3}:({:6},{:6}), {:3}:({:6},{:6}), {:3}:({:7},{:7}), {:3}:({:5},{:7}),".format(
            angle,      x,  y,
            angle+90,  -y,  x,
            angle+180, -x, -y,
            angle+270,  y, -x,))
print("}")
print()

